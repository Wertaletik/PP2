x=3
y=5


#AND
print(x!=y and x>y) #True and False = False
print(x!=y and x<y) #True and True = True
print(x==y and x>=y) #False and False = False


#OR
print(x!=y or x>y) #True or False = True
print(x!=y or x<y) #True or True = True
print(x==y or x>=y) #False or False = False


#NOT
print(not x<=y) #not True = False
print(not x==y) #not False = True